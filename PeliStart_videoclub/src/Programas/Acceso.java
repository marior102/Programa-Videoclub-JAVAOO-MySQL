    private void jbtnAccederActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnAccederActionPerformed
        String codUsuario = txtcodUsuario.getText();
        if (bd.existeUsuarioClave( codUsuario, txtusuario.getText(), jpasword.getText())) {
            JOptionPane.showMessageDialog(null, "Ingreso con exito", "Bienvendidos", JOptionPane.INFORMATION_MESSAGE);
            try {
                this.hide();
                this.dispose();
                this.finalize();
            } catch (Throwable ex) {
                ex.printStackTrace();
            }
           VistaPrincipal m = new VistaPrincipal();
           
            m.setVisible(true);
           setExtendedState(javax.swing.JFrame.MAXIMIZED_BOTH);
            m.setLocationRelativeTo(null);
        } else {
            JOptionPane.showMessageDialog(null, "Disculpe usted no esta autorizado", "Intento Fallido",
                    JOptionPane.INFORMATION_MESSAGE);
            JOptionPane optionpane = new JOptionPane();
            Object[] opciones = {"si", "no"};
            int ret = JOptionPane.showOptionDialog(this, "Desea salir?", "Pregunta", JOptionPane.YES_NO_OPTION,
                    JOptionPane.QUESTION_MESSAGE, null, opciones, opciones[0]);
            if (ret == JOptionPane.YES_NO_OPTION) {
                System.exit(0);
            }
        }



    }//GEN-LAST:event_jbtnAccederActionPerformed

